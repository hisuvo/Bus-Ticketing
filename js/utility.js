function $(id){
    return document.getElementById(id);
}

function $nule(id){
    return document.getElementById(id).value = ""
}

function $value(id){
    return document.getElementById(id).value;
}

function $text(id){
    return document.getElementById(id).innerText;
}

function $element(id){
    return document.getElementById(id).innerHTML;
}

function $togle(id){
    return document.getElementById(id)
}